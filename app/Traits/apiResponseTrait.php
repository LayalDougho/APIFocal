<?php 
namespace App\Traits;
trait ApiResponseTrait{

    protected function success( $data ,$message=null, $status = 200)
    {
        return response([
            'success' => true,
            'data' => $data,
            'message' => $message,
        ], $status);
    }

    protected function failure($message, $status = 422)
    {
        return response([
            'success' => false,
            'message' => $message,
        ], $status);
    }
}






?>